#include <iostream>
#include <cmath>
using namespace std;
int main() {
    // Mathematical constants
    
    double euler = M_E;
    double ln2 = M_LN2;
    double ln10 = M_LN10;
    double log2e = M_LOG2E;
    double log10e = M_LOG10E;
    // Displaying the constants
    cout << "Euler's number (e): " << euler << endl;
    cout << "natural logrithm of 2 (ln(2)): " << ln2 << endl;
    cout << "natural logarithm of 10 (ln(10)): " << ln10 << endl;
    cout << "Base-2 logarithm of e (log2(e)): " << log2e << endl;
    cout << "Base-10 logarithm of e (log10(e)): " << log10e << endl;
    return 0;
}